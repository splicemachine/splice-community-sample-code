create schema CENSUS;

create table CENSUS.TRAINING_DATA (
	AGE INT,
	WORKCLASS VARCHAR(20),
	FNLWGT INT,
	EDUCATION VARCHAR(20),
	EDUCATION_NUM INT,
	MARITAL_STATUS VARCHAR(20),
	OCCUPATION VARCHAR(20),
	RELATIONSHIP VARCHAR(20),
	RACE VARCHAR(20),
	GENDER VARCHAR(20),
	CAPITAL_GAIN INT,
	CAPITAL_LOSS INT,
	HOURS_PER_WEEK INT,
	NATIVE_COUNTRY VARCHAR(20),
	INCOME_BRACKET VARCHAR(20),
	LABEL VARCHAR(20)
);

create table CENSUS.TESTING_DATA (
	AGE INT,
	WORKCLASS VARCHAR(20),
	FNLWGT INT,
	EDUCATION VARCHAR(20),
	EDUCATION_NUM INT,
	MARITAL_STATUS VARCHAR(20),
	OCCUPATION VARCHAR(20),
	RELATIONSHIP VARCHAR(20),
	RACE VARCHAR(20),
	GENDER VARCHAR(20),
	CAPITAL_GAIN INT,
	CAPITAL_LOSS INT,
	HOURS_PER_WEEK INT,
	NATIVE_COUNTRY VARCHAR(20),
	INCOME_BRACKET VARCHAR(20),
	LABEL VARCHAR(20)
);

create table CENSUS.LIVE_DATA (
	ID int generated by default as identity (START WITH 2, INCREMENT BY 1),
	AGE INT,
	WORKCLASS VARCHAR(20),
	FNLWGT INT,
	EDUCATION VARCHAR(20),
	EDUCATION_NUM INT,
	MARITAL_STATUS VARCHAR(20),
	OCCUPATION VARCHAR(20),
	RELATIONSHIP VARCHAR(20),
	RACE VARCHAR(20),
	GENDER VARCHAR(20),
	CAPITAL_GAIN INT,
	CAPITAL_LOSS INT,
	HOURS_PER_WEEK INT,
	NATIVE_COUNTRY VARCHAR(20),
	INCOME_BRACKET VARCHAR(20),
	LABEL VARCHAR(20)
);

create table MODEL_DICTIONARY (
	ID int generated by default as identity (START WITH 2, INCREMENT BY 1),
	MODEL varchar(50),
	COLUMN_NAME varchar(50),
	TYPE varchar(50),
	SEQUENCE int,
	GROUPING int,
	LABEL varchar(50),
	GROUPING_DETAILS varchar(50),
	GROUPING_DETAILS_TYPE varchar(50)
);

